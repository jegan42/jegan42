#include "libft.h"

int	ft_nbw(const char *s, char c)
{
	int		i;
	int		nbw;

	nbw = 0;
	i = 0;
	if (!s)
		return ((int)0);
	while (s[i])
	{
		while (s[i] && (s[i] == c))
			++i;
		if (s[i] && !(s[i] == c))
			++nbw;
		while (s[i] && !(s[i] == c))
			++i;
	}
	return (nbw);
}
